import '#app/globals.css';
import type { Metadata } from 'next';
import { LinguiClientProvider } from '#app/_components/lingui-client-provider.tsx';
import { inter } from '#libs/Fonts.ts';
import { cn } from '#libs/cn.ts';
import { allMessages } from '#libs/locales/appRouterI18n.ts';
import linguiConfig from '#libs/locales/lingui.config.ts';
import { withLinguiLayout } from '#libs/locales/withLingui.tsx';

export const metadata: Metadata = {
    title: 'Create Next App',
    description: 'Generated by create next app',
};

export default withLinguiLayout(function RootLayout({
    children,
    params: { lang },
}) {
    return (
        <html lang={lang}>
            <LinguiClientProvider
                initialLocale={lang}
                initialMessages={allMessages[lang]}
            >
                <body
                    className={cn(
                        'antialiased [font-synthesis-weight:none]',
                        inter.variable,
                    )}
                >
                    {children}
                </body>
            </LinguiClientProvider>
        </html>
    );
});

export async function generateStaticParams() {
    return linguiConfig.locales.map((lang) => ({ lang }));
}
